    /**
     * This function add/update category in DB
     * @param Blog $blog
     * @param bool $flush
     * @return Blog
     */
    public function save(Blog $blog, bool $flush = false): Blog {
        $em = $this->getEntityManager();
        $em->persist($blog);
        
        if($flush == true) {
            $em->flush();
        }
        
        return $blog;
